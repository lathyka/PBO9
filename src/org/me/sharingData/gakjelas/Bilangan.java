
package org.me.sharingData.gakjelas;

import org.me.masalahdua.*;


public class Bilangan 
{
    private int  x = 1;

    public int getX() {
        return x;
    }

   public  void  tambahXnya()
   {
       for(int  i = 0; i<2000;i++)
       x++;
   }
   
    public  void  kurangXnya()
   {
       for(int  i = 0; i<2000;i++)
       x--;
   }
   
 
    
    
}
